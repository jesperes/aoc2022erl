unsigned int priv_input18_txt_len = 15589;
unsigned char priv_input18_txt[];

typedef struct {
  int x;
  int y;
  int z;
} cube_t;

cube_t mkcube(int x, int y, int z) {
  cube_t c = {x, y, z};
  return c;
}

#define MAXCUBES 16384
#define OFFSET 2
#define SIZE 25

typedef bool cave_t[SIZE][SIZE][SIZE];

#define CAVE_AT(cave, cube) (cave[(cube).x + OFFSET][(cube).y + OFFSET][(cube).z + OFFSET])

int64_t now()
{
  struct timespec ts;
  clock_gettime(CLOCK_REALTIME, &ts);
  return ts.tv_sec * 1000000000 + ts.tv_nsec;
}

typedef struct {
  int64_t p1;
  int64_t p2;
} solution_t;

int is_adjacent(cube_t *c1, cube_t *c2)
{
  return
    abs(c1->x - c2->x) +
    abs(c1->y - c2->y) +
    abs(c1->z - c2->z) == 1;
}



void fill(cube_t cube, cave_t cubemap, cave_t water, int64_t *area) {
  if (cube.x < -1 ||
      cube.x > 20 ||
      cube.y < -1 ||
      cube.y > 20 ||
      cube.z < -1 ||
      cube.z > 20) {
    return;
  }

  if (CAVE_AT(cubemap, cube)) {
    // We have reached a lava cube, increase surface area, but do nothing else
    (*area)++;
  } else if (CAVE_AT(water, cube)) {
    // Already been here, just skip this
    return;
  } else {
    // New unfilled cube reached, fill it with water.
    CAVE_AT(water, cube) = true;
    fill(mkcube(cube.x + 1, cube.y, cube.z), cubemap, water, area);
    fill(mkcube(cube.x - 1, cube.y, cube.z), cubemap, water, area);
    fill(mkcube(cube.x, cube.y + 1, cube.z), cubemap, water, area);
    fill(mkcube(cube.x, cube.y - 1, cube.z), cubemap, water, area);
    fill(mkcube(cube.x, cube.y, cube.z + 1), cubemap, water, area);
    fill(mkcube(cube.x, cube.y, cube.z - 1), cubemap, water, area);
  }
}

void solve(solution_t *sol)
{
  cube_t cubes[MAXCUBES] = { 0 };
  char *p = (char *)priv_input18_txt;
  int cube_idx = 0;

  for (int i = 0; i < priv_input18_txt_len; i++) {
    cube_t cube;
    char *endptr;
    cube.x = strtol(p, &endptr, 10);
    p = endptr + 1;
    cube.y = strtol(p, &endptr, 10);
    p = endptr + 1;
    cube.z = strtol(p, &endptr, 10);
    p = endptr + 1;
    i = endptr - (char *)priv_input18_txt;
    cubes[cube_idx++] = cube;
  }

  int num_cubes = cube_idx;
  sol->p1 = num_cubes * 6;
  for (int i = 0; i < num_cubes; i++) {
    for (int j = 0; j < num_cubes; j++) {
      if (i < j) {
        if (is_adjacent(&cubes[i], &cubes[j])) {
          sol->p1 -= 2;
        }
      }
    }
  }

  cube_t start = {0, 0, 0};
  cave_t water = {0};
  cave_t cubemap = {0};
  for (int i = 0; i < num_cubes; i++) {
    cube_t cube = cubes[i];
    CAVE_AT(cubemap, cube) = true;
  }

  fill(start, cubemap, water, &sol->p2);
}

int main(int argc, char *argv[])
{
  // test
  solution_t sol = {0};
  solve(&sol);
  assert(sol.p1 == 3530);
  assert(sol.p2 == 2000);

  // timing
  int reps = 100;
  int64_t total_elapsed = 0;
  for (int i = 0; i < reps; i++) {
    int64_t t0 = now();
    solve(&sol);
    int64_t t1 = now();
    total_elapsed += (t1 - t0);
  }

  int64_t avg_ns_per_iter = total_elapsed / reps;
  printf("Average execution time: %ld Î¼s\n", (avg_ns_per_iter / 1000));
}

